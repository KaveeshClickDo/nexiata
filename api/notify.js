// ── Best-effort in-memory rate limiter ──────────────────────────────
// Lives only as long as the serverless instance stays warm. It won't stop a
// large distributed attack, but it blocks the common case: one IP hammering
// the form. Honeypot + timing checks below catch most automated submitters.
const RATE = new Map(); // ip -> [timestamps]
const WINDOW_MS = 10 * 60 * 1000; // 10 minutes
const MAX_PER_WINDOW = 5;
const MIN_FILL_MS = 2500; // form submitted faster than this == bot

function isRateLimited(ip) {
  const now = Date.now();
  const hits = (RATE.get(ip) || []).filter(t => now - t < WINDOW_MS);
  hits.push(now);
  RATE.set(ip, hits);
  if (RATE.size > 5000) RATE.clear(); // guard against unbounded growth
  return hits.length > MAX_PER_WINDOW;
}

export default async function handler(req, res) {
  if (req.method !== 'POST') {
    return res.status(405).json({ error: 'Method not allowed' });
  }

  const { email, company, elapsed } = req.body ?? {};

  // 1. Honeypot — real users never fill the hidden "company" field.
  //    Pretend success so bots don't learn they were caught.
  if (company) {
    return res.status(200).json({ success: true });
  }

  // 2. Timing — a submit that arrives instantly (or with no timing token at
  //    all, i.e. a direct API post) is almost certainly automated.
  if (typeof elapsed !== 'number' || elapsed < MIN_FILL_MS) {
    return res.status(200).json({ success: true });
  }

  // 3. Validate the email (format + sane length).
  if (!email || typeof email !== 'string' || email.length > 254 ||
      !/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email)) {
    return res.status(400).json({ error: 'Invalid email address' });
  }

  // 4. Rate limit per IP.
  const ip = (req.headers['x-forwarded-for'] || '').split(',')[0].trim() || 'unknown';
  if (isRateLimited(ip)) {
    return res.status(429).json({ error: 'Too many requests' });
  }

  const apiKey = process.env.RESEND_API_KEY;
  if (!apiKey) {
    console.error('RESEND_API_KEY not set');
    return res.status(500).json({ error: 'Service unavailable' });
  }

  const headers = {
    Authorization: `Bearer ${apiKey}`,
    'Content-Type': 'application/json',
  };

  const safeEmail = email.replace(/[<>&"']/g, c =>
    ({ '<': '&lt;', '>': '&gt;', '&': '&amp;', '"': '&quot;', "'": '&#39;' }[c]));

  try {
    // 1. Notify owners — this is the critical path
    const ownerRes = await fetch('https://api.resend.com/emails', {
      method: 'POST',
      headers,
      body: JSON.stringify({
        from:     'NEXIATA <hello@nexiata.com>',
        to:       ['hello@nexiata.com', 'khsenevirathne@gmail.com', 'thenexiata@gmail.com'],
        subject:  'NEXIATA: New launch subscriber',
        html:     `<p style="font-family:Arial,sans-serif;color:#333;">New subscriber signed up for the NEXIATA launch:</p><p style="font-family:'Courier New',monospace;font-size:16px;color:#000;"><strong>${safeEmail}</strong></p>`,
        reply_to: email,
      }),
    });

    if (!ownerRes.ok) {
      const body = await ownerRes.json().catch(() => ({}));
      console.error('Resend owner email error:', body);
      return res.status(502).json({ error: 'Notification failed' });
    }

    // 2. Thank-you email to subscriber — non-blocking, failure doesn't affect the response
    fetch('https://api.resend.com/emails', {
      method: 'POST',
      headers,
      body: JSON.stringify({
        from:    'NEXIATA <hello@nexiata.com>',
        to:      [email],
        subject: 'NEXIATA: You are on the list',
        html:    thankYouHtml(),
      }),
    }).catch(err => console.error('Thank-you email failed (non-critical):', err));

    return res.status(200).json({ success: true });
  } catch (err) {
    console.error('Notify handler error:', err);
    return res.status(500).json({ error: 'Internal error' });
  }
}
